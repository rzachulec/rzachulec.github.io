                    instalacyjnego
                    domów rodzi pytania o jej efekt z perspektywy mieszkańców. Czy utrzymanie komfortu wymaga
                    zaangażowania
                    mieszkańców, czy nie? Jakie jest znaczenie takich czy innych sposobów korzystania z dostępnych
                    technologii dla
                    komfortu i zużycia energii?
                </p1>
